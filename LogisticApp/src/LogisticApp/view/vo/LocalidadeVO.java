package LogisticApp.view.vo;

public class LocalidadeVO {
	
	private int id;
	private String descricao;
	
	public LocalidadeVO(int id, String descricao) {
		this.id = id;
		this.descricao = descricao;
	}

	public int getId() {
		return id;
	}

	public String getDescricao() {
		return descricao;
	}

}
